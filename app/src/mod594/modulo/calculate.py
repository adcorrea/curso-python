


def soma(a, b) -> float:
    assert isinstance(a, (int, float)), 'x percisa ser int ou float'
    assert isinstance(b, (int, float)), 'x percisa ser int ou float'

    return a + b


def subtrai(a, b) -> float:
    assert isinstance(a, (int, float)), 'x percisa ser int ou float'
    assert isinstance(b, (int, float)), 'x percisa ser int ou float'
    
    return a - b